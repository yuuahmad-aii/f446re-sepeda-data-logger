// Implementasi fungsi untuk MPU6050
void MPU6050_Init(I2C_HandleTypeDef *hi2c)
{
  uint8_t check;
  uint8_t Data;

  // Periksa ID perangkat
  HAL_I2C_Mem_Read(hi2c, MPU6050_ADDR, WHO_AM_I_REG, 1, &check, 1, 1000);

  if (check == 0x68) // 0x68 adalah nilai default dari WHO_AM_I
  {
    // Bangunkan sensor dari mode tidur
    Data = 0;
    HAL_I2C_Mem_Write(hi2c, MPU6050_ADDR, PWR_MGMT_1_REG, 1, &Data, 1, 1000);

    // Atur Sample Rate ke 1KHz
    Data = 0x07;
    HAL_I2C_Mem_Write(hi2c, MPU6050_ADDR, SMPLRT_DIV_REG, 1, &Data, 1, 1000);

    // Atur konfigurasi Akselerometer ke ±2g
    Data = 0x00;
    HAL_I2C_Mem_Write(hi2c, MPU6050_ADDR, ACCEL_CONFIG_REG, 1, &Data, 1, 1000);

    // Atur konfigurasi Giroskop ke ±250°/s
    Data = 0x00;
    HAL_I2C_Mem_Write(hi2c, MPU6050_ADDR, GYRO_CONFIG_REG, 1, &Data, 1, 1000);
  }
}

void MPU6050_Read_Accel(I2C_HandleTypeDef *hi2c, MPU6050_t *DataStruct)
{
  uint8_t Rec_Data[6];

  // Baca 6 byte data dari register ACCEL_XOUT_H
  HAL_I2C_Mem_Read(hi2c, MPU6050_ADDR, ACCEL_XOUT_H_REG, 1, Rec_Data, 6, 1000);

  DataStruct->Accel_X_RAW = (int16_t)(Rec_Data[0] << 8 | Rec_Data[1]);
  DataStruct->Accel_Y_RAW = (int16_t)(Rec_Data[2] << 8 | Rec_Data[3]);
  DataStruct->Accel_Z_RAW = (int16_t)(Rec_Data[4] << 8 | Rec_Data[5]);

  // Konversi nilai mentah ke 'g'
  DataStruct->Ax = DataStruct->Accel_X_RAW / 16384.0;
  DataStruct->Ay = DataStruct->Accel_Y_RAW / 16384.0;
  DataStruct->Az = DataStruct->Accel_Z_RAW / 16384.0;
}

void MPU6050_Read_Gyro(I2C_HandleTypeDef *hi2c, MPU6050_t *DataStruct)
{
  uint8_t Rec_Data[6];

  // Baca 6 byte data dari register GYRO_XOUT_H
  HAL_I2C_Mem_Read(hi2c, MPU6050_ADDR, GYRO_XOUT_H_REG, 1, Rec_Data, 6, 1000);

  DataStruct->Gyro_X_RAW = (int16_t)(Rec_Data[0] << 8 | Rec_Data[1]);
  DataStruct->Gyro_Y_RAW = (int16_t)(Rec_Data[2] << 8 | Rec_Data[3]);
  DataStruct->Gyro_Z_RAW = (int16_t)(Rec_Data[4] << 8 | Rec_Data[5]);

  // Konversi nilai mentah ke °/s
  DataStruct->Gx = DataStruct->Gyro_X_RAW / 131.0;
  DataStruct->Gy = DataStruct->Gyro_Y_RAW / 131.0;
  DataStruct->Gz = DataStruct->Gyro_Z_RAW / 131.0;
}

