/**
 * @brief  Function implementing the defaultTask thread.
 * @param  argument: Not used
 * @retval None
 */
/* USER CODE END Header_StartDefaultTask */
void StartDefaultTask(void *argument)
{
  /* USER CODE BEGIN 5 */
  /* Infinite loop */
  for (;;)
  {
    // Baca data sensor
    MPU6050_Read_Accel(&hi2c1, &MPU6050_Data);
    MPU6050_Read_Gyro(&hi2c1, &MPU6050_Data);

    // Cetak data ke serial monitor
    printf("Ax: %.2f, Ay: %.2f, Az: %.2f | Gx: %.2f, Gy: %.2f, Gz: %.2f\r\n",
           MPU6050_Data.Ax, MPU6050_Data.Ay, MPU6050_Data.Az,
           MPU6050_Data.Gx, MPU6050_Data.Gy, MPU6050_Data.Gz);

    osDelay(500); // Tunda selama 500 milidetik
  }
  /* USER CODE END 5 */
}

